// The JavaScript for of statement loop over iterable data structures such as Arrays, Strings, Maps, NodeLists, and more.

// Looping Over an Array
const cars = ["BMW", "Volvo", "Mini"];

for (let x of cars) {
  console.log(x);
}

// Looping Over an String
const language = "Javascript";

for (let x of language) {
    console.log(x);
}